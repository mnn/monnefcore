package monnef.core.utils;

public class BoundingBoxSize {
    public float x1, x2, y1, y2, z1, z2;

    public BoundingBoxSize(float x1, float x2, float y1, float y2, float z1, float z2) {
        this.x1 = x1;
        this.x2 = x2;
        this.y1 = y1;
        this.y2 = y2;
        this.z1 = z1;
        this.z2 = z2;
    }
}
